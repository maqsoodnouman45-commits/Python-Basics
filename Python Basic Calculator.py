#Create a calculator capable of performing basic arithmetic operations like addition, subtraction, multiplication, and division.

no_1=9
no_2=5

print("Addition of two numbers is:",no_1 + no_2) # Addition
print("Subtraction of two numbers is:",no_1 - no_2) # Subtraction
print("Multiplication of two numbers is:",no_1 * no_2) # Multiplication
print("Division of two numbers is:",no_1 / no_2) # Division
print("Modulus of two numbers is:",no_1 % no_2) # Modulus
print("Floor Division of two numbers is:",no_1 // no_2) # Floor Division
print("Exponential form of two numbers is:",no_1 ** no_2) # Exponential